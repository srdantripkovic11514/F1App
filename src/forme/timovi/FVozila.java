    private void format() {
        setIconImage(new ImageIcon(getClass().getResource("Icon.png")).getImage());
        setLocationRelativeTo(null);
        setVisible(true);
    }

    public void popuniKomboTimovi(Odgovor o) {
        jComboTim.removeAllItems();
        ArrayList<Tim> timovi = (ArrayList<Tim>) o.getPodaci();

        for (Tim tim : timovi) {
            jComboTim.addItem(tim);
        }
    }

    private void vratiTimove() {
        Zahtev z = new Zahtev();
        z.setOperacija(Operacija.VRATI_TIMOVE);

        Komunikacija.getInstance().posaljiZahtev(z);
    }

    private void postaviModel() {
        TabelaVozilaModel model = new TabelaVozilaModel();
        jTable1.setModel(model);
    }

    public void ubaciVozilaUTabelu(Odgovor o) {
        TabelaVozilaModel m = (TabelaVozilaModel) jTable1.getModel();
        m.setLista((ArrayList<Vozilo>) o.getPodaci());
        m.fireTableDataChanged();
    }

    public void osveziModel(ArrayList<Vozilo> vozila) {
        TabelaVozilaModel m = (TabelaVozilaModel) jTable1.getModel();
        m.setLista(vozila);
        m.fireTableDataChanged();

    }

    public void obrisano(Odgovor o) {
        JOptionPane.showMessageDialog(this, o.getPoruka());
        
    }

    public void sacuvano(Odgovor o) {
        JOptionPane.showMessageDialog(this, o.getPoruka());
        TabelaVozilaModel m = (TabelaVozilaModel) jTable1.getModel();
        m.getLista().add((Vozilo) o.getPodaci());
        m.fireTableDataChanged();
    }

