    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Tim t = (Tim) jComboTim.getSelectedItem();

            Vozilo v = new Vozilo();
            v.setTim(t);

            Zahtev z = new Zahtev(Operacija.VRATI_VOZILA, v);

            Komunikacija.getInstance().posaljiZahtev(z);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Nije moguce vratiti podatke o vozilima");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String motor = jTextMotor.getText().trim();
        String sasija = jTextSasija.getText().trim();
        if (!motor.equals("") && !sasija.equals("")) {
            Tim t = (Tim) jComboTim.getSelectedItem();

            Vozilo v = new Vozilo();
            v.setOznakaMotora(motor);
            v.setOznakaSasije(sasija);
            v.setTim(t);
            TabelaVozilaModel m = (TabelaVozilaModel) jTable1.getModel();
            if(m.getLista().size()>=4){
                JOptionPane.showMessageDialog(this, "Tim u jednom trenutku moze imati najvise 4 vozila");
                return;
            }
            if(m.getLista().contains(v)){
                JOptionPane.showMessageDialog(this, "Vozilo sa unetim podacima vec postoji");
                return;
            }
            Zahtev z = new Zahtev(Operacija.SACUVAJ_VOZILO, v);

            Komunikacija.getInstance().posaljiZahtev(z);
            //m.getLista().add(v);
            //m.fireTableDataChanged();
        } else {
            JOptionPane.showMessageDialog(this, "Svi podaci moraju biti uneti!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            TabelaVozilaModel m = (TabelaVozilaModel) jTable1.getModel();
            Vozilo v = m.getLista().get(jTable1.getSelectedRow());

            Zahtev z = new Zahtev(Operacija.IZBRISI_VOZILO, v);

            Komunikacija.getInstance().posaljiZahtev(z);
            m.getLista().remove(v);
            m.fireTableDataChanged();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Odaberite vozilo koje želite da obrišete");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

