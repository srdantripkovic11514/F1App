    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = jTable1.getRowSorter().convertRowIndexToModel(jTable1.getSelectedRow());
        TabelaTimModel m = (TabelaTimModel) jTable1.getModel();
        Tim t = m.getLista().get(selectedRow);
        jTextField3.setEnabled(true);
        jTextField2.setEnabled(true);
        jTextField1.setEnabled(false);
        jTable1.setEnabled(false);
        jTextField2.setText(t.getIzvrsniDirektor());
        jTextField3.setText(t.getGlavniTehnicar());
        revalidate();
        repaint();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = jTable1.getRowSorter().convertRowIndexToModel(jTable1.getSelectedRow());
        TabelaTimModel m = (TabelaTimModel) jTable1.getModel();
        Tim t = m.getLista().get(selectedRow);
        String izvrsni  = jTextField2.getText().trim();
        String glavni = jTextField3.getText().trim();
        if (izvrsni.matches(".*\\d.*") || glavni.matches(".*\\d.*")) {
            JOptionPane.showMessageDialog(this, "Unete vrednosti ne smeju sadrzati brojeve");
            return;
        }
        if(izvrsni.equals("") || glavni.equals("")){
            JOptionPane.showMessageDialog(this, "U oba polja moraju biti unete vrednosti");
            return;
        }
        t.setIzvrsniDirektor(izvrsni);
        t.setGlavniTehnicar(glavni);
        Zahtev z = new Zahtev(Operacija.IZMENI_TIM, t);
        
        Komunikacija.getInstance().posaljiZahtev(z);
        
        restart();
    }//GEN-LAST:event_jButton2ActionPerformed

