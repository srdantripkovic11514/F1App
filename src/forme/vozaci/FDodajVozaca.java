    public void popuniKomboDrzave(Odgovor o) {
        jComboBox1.removeAllItems();
        ArrayList<Drzava> drzave = (ArrayList<Drzava>) o.getPodaci();

        for (Drzava drzava : drzave) {
            jComboBox1.addItem(drzava);
        }

    }

    public void popuniKomboTimove(Odgovor o) {
        jComboBox2.removeAllItems();
        ArrayList<Tim> timovi = (ArrayList<Tim>) o.getPodaci();

        for (Tim tim : timovi) {
            jComboBox2.addItem(tim);
        }

    }

