    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String ime = jTextField1.getText().trim();
            String prezime = jTextField2.getText().trim();
            int dd = Integer.valueOf(jTextField3.getText().trim());
            int mm = Integer.valueOf(jTextField4.getText().trim());
            int yyyy = Integer.valueOf(jTextField5.getText().trim());
            int takBr = Integer.valueOf(jTextField6.getText().trim());
            int brPob = Integer.valueOf(jTextField7.getText().trim());
            if (takBr < 0 || brPob < 0) {
                JOptionPane.showMessageDialog(this, "Vrednosti unete za takmicarski broj i broj pobeda moraju biti pozitivne");
                return;
            }
            if (!ime.equals("") && !prezime.equals("") && yyyy > 1900 && yyyy < LocalDate.now().getYear() + 1) {
                LocalDate datRodj = LocalDate.of(yyyy, mm, dd);
                Tim t = (Tim) jComboBox2.getSelectedItem();
                Drzava d = (Drzava) jComboBox1.getSelectedItem();

                Vozac v = new Vozac();
                v.setDrzava(d);
                v.setIme(ime);
                v.setPrezime(prezime);
                v.setTim(t);
                v.setDatumRodjenja(datRodj);
                v.setBrojPobeda(brPob);
                v.setTakmicarskiBroj(takBr);

                Zahtev z = new Zahtev(Operacija.SACUVAJ_VOZACA, v);

                Komunikacija.getInstance().posaljiZahtev(z);

            } else {
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena");
            }
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "Unesite ispravne vrednosti u polja za unos datuma rođenja, "
                    + "takmičarskog broja i broja pobeda");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

