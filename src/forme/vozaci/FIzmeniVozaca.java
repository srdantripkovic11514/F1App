    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = jTable1.getRowSorter().convertRowIndexToModel(jTable1.getSelectedRow());
        TabelaVozacIzmenaModel m = (TabelaVozacIzmenaModel) jTable1.getModel();
        Tim t = (Tim) jComboBox1.getSelectedItem();
        Vozac v = m.getLista().get(selectedRow);
        int takBr = v.getTakmicarskiBroj();
        try {
            takBr = Integer.valueOf(jTextField2.getText().trim());
            if (takBr<0) {
                throw new NumberFormatException();
            }
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "Unesite validnu vrednost u polje Takmicarski broj");
            return;
        }
        v.setTim(t);
        v.setTakmicarskiBroj(takBr);
        
        Zahtev z = new Zahtev(Operacija.IZMENI_VOZACA, v);
        
        Komunikacija.getInstance().posaljiZahtev(z);
        
        restart();
    }//GEN-LAST:event_jButton3ActionPerformed

