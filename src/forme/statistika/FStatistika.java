    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int sezona = Integer.valueOf((String) jComboBox1.getSelectedItem());
        ArrayList<Rezultat> tabela = new ArrayList<>();
        for (Vozac vozac : voz) {
            int poeni = 0;
            boolean b = false;
            for (Rezultat rezultat : rez) {
                if (rezultat.getTrka().getSezona() == sezona && rezultat.getVozac().equals(vozac)) {
                    poeni = poeni + rezultat.getBrojPoena();
                    b = true;
                }
            }
            if (b) {
                Rezultat r = new Rezultat();
                r.setBrojPoena(poeni);
                r.setVozac(vozac);
                tabela.add(r);
            }

        }
        Collections.sort(tabela, rezCom);
        TabelaStatistikaVozaciModel m = (TabelaStatistikaVozaciModel) jTable1.getModel();
        
        m.setLista(tabela);
        m.fireTableDataChanged();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int sezona = Integer.valueOf((String) jComboBox2.getSelectedItem());
        ArrayList<Rezultat> tabela = new ArrayList<>();
        for (Tim tim : tim) {
            int poeni = 0;
            boolean b = false;
            for (Rezultat rezultat : rez) {
                if (rezultat.getTrka().getSezona() == sezona && rezultat.getVozac().getTim().equals(tim)) {
                    poeni = poeni + rezultat.getBrojPoena();
                    b = true;
                }
            }
            if (b) {
                Rezultat r = new Rezultat();
                r.setBrojPoena(poeni);
                r.setVozac(new Vozac());
                r.getVozac().setTim(tim);
                tabela.add(r);
            }

        }
        Collections.sort(tabela, rezCom);
        TabelaStatistikaTimModel m = (TabelaStatistikaTimModel) jTable2.getModel();

        m.setLista(tabela);
        m.fireTableDataChanged();
    }//GEN-LAST:event_jButton2ActionPerformed

