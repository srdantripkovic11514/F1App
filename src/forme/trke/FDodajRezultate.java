    public void vratiRezultateTrke(Odgovor o) {
        ArrayList<Rezultat> rez = (ArrayList<Rezultat>) o.getPodaci();
        if (!rez.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Za odabranu trku vec su uneti rezultati, pogledajte ih na formi trke");
            restart();
        }
    }

