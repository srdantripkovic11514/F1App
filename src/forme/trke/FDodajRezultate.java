    public void postaviModel(Odgovor o) {
        @SuppressWarnings("unchecked")
        TabelaTrkaIzmenaModel model = new TabelaTrkaIzmenaModel((ArrayList<Trka>) o.getPodaci());
        ArrayList<Trka> trkeIzm = new ArrayList<>();
        for (Trka trka : model.getTrke()) {
            if (trka.getDatumOdrzavanja().isBefore(LocalDateTime.now())) {
                trkeIzm.add(trka);
            }
        }
        model.setTrke(trkeIzm);
        jTable2.setModel(model);

        tabelaSorterFilter();
    }

