    public void postaviModel(Odgovor o) {
        @SuppressWarnings("unchecked")
        TableModel model = new TabelaTrkaIzmenaModel((ArrayList<Trka>) o.getPodaci());
        jTable1.setModel(model);

        tabelaSorterFilter();
    }
    
    private void postaviModelRezultata() {
        @SuppressWarnings("unchecked")
        TableModel model = new TabelaDodajRezultateModel();
        jTable2.setModel(model);
    }

    private void vratiTrke() {
        Zahtev z = new Zahtev();
        z.setOperacija(Operacija.VRATI_TRKE);

        Komunikacija.getInstance().posaljiZahtev(z);
    }
    
    private void vratiRezultateTrke(Trka t) {
        Zahtev z = new Zahtev();
        Rezultat r = new Rezultat();
        r.setTrka(t);
        z.setOperacija(Operacija.VRATI_REZULTATE_TRKE);
        z.setPodaci(r);

        Komunikacija.getInstance().posaljiZahtev(z);
    }

    private void tabelaSorterFilter() {

        TableRowSorter<TableModel> rowSorter = new TableRowSorter<>(jTable1.getModel());
        jTable1.setRowSorter(rowSorter);

        jTextField1.getDocument().addDocumentListener(new DocumentListener() {

            @Override
            public void insertUpdate(DocumentEvent e) {
                String text = jTextField1.getText();

                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                String text = jTextField1.getText();

                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
            }
        });
    }

    public void prikaziRezultateTrke(Odgovor o) {
        TabelaDodajRezultateModel m = (TabelaDodajRezultateModel) jTable2.getModel();
        m.setLista((ArrayList<Rezultat>) o.getPodaci());
        Collections.sort(m.getLista(), rezCom);
        m.fireTableDataChanged();
    }
    
    public static Comparator<Rezultat> rezCom = (Rezultat r1, Rezultat r2) -> {
        int r1Com = r1.getKonacnaPozicija();
        int r2Com = r2.getKonacnaPozicija();

        return r1Com - r2Com;
    };
