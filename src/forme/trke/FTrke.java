    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        TabelaTrkaIzmenaModel m = (TabelaTrkaIzmenaModel) jTable1.getModel();
        try {
            int i = jTable1.getRowSorter().convertRowIndexToModel(jTable1.getSelectedRow());
        } catch (Exception e) {
            if (!jTextField1.getText().isEmpty() && jTable1.getRowCount()==0) {
                JOptionPane.showMessageDialog(this, "Ne postoji trka sa traženim parametrima");
                return;
            }
            JOptionPane.showMessageDialog(this, "Odaberite trku za prikaz podataka");
            return;
        }
        Trka t = m.getTrke().get(jTable1.getRowSorter().convertRowIndexToModel(jTable1.getSelectedRow()));
        vratiRezultateTrke(t);
        try {
            jlblNaziv.setText(t.getNaziv());
            jlblStaza.setText(t.getStaza().getNaziv());
            jlblDrzava.setText(t.getStaza().getDrzava().getNaziv());
            jlblDatumVeme.setText(t.getDatumOdrzavanja().getDayOfMonth() + "." + t.getDatumOdrzavanja().getMonthValue() + "." + t.getDatumOdrzavanja().getYear() + " "
                    + t.getDatumOdrzavanja().getHour() + ":" + t.getDatumOdrzavanja().getMinute());
            jlblSezona.setText(String.valueOf(t.getSezona()));
            jlblBrKr.setText(String.valueOf(t.getBrojkrugova()));
            jlblDuzina.setText(String.valueOf(t.getDuzinaTrke() / 1000.0));
            jlblNajbrziKrug.setText(t.getNajbrziKrug() / 60000 + ":" + (t.getNajbrziKrug() % 60000) / 1000 + "." + (t.getNajbrziKrug() % 60000) % 1000);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Nije moguće prikazati podatke o trci");
        }
        revalidate();
        repaint();
    }//GEN-LAST:event_jButton1ActionPerformed

