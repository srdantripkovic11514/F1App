    public void popuniKomboStaze(Odgovor o) {
        jComboBox1.removeAllItems();
        ArrayList<Staza> staze = (ArrayList<Staza>) o.getPodaci();
        
        for (Staza staza : staze) {
            jComboBox1.addItem(staza);
        }
        
    }
    
    private void vratiStaze() {
        Zahtev z = new Zahtev();
        z.setOperacija(Operacija.VRATI_STAZE);
        
        Komunikacija.getInstance().posaljiZahtev(z);
    }

