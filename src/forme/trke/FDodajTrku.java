    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String naziv = jTextField1.getText().trim();
            if(naziv.equals("")){
                JOptionPane.showMessageDialog(this, "Unesite naziv trke");
                return;
            }
            int brKr = Integer.valueOf(jTextField2.getText().trim());
            if (brKr<1) {
                JOptionPane.showMessageDialog(this, "Broj krugova mora biti veci od 0");
                return;
            }
            int dd = Integer.valueOf((String)jComboBox2.getSelectedItem());
            int MM = Integer.valueOf((String)jComboBox3.getSelectedItem());
            int yyyy = Integer.valueOf((String)jComboBox4.getSelectedItem());
            
            int hh = (int) jSpinner2.getValue();
            int mm = (int) jSpinner1.getValue();
            LocalDateTime datumOdrzavanja = LocalDateTime.of(yyyy, MM, dd, hh, mm);
            Staza staza = (Staza) jComboBox1.getSelectedItem();
            
            Trka trka = new Trka();
            trka.setBrojkrugova(brKr);
            trka.setNaziv(naziv);
            trka.setDatumOdrzavanja(datumOdrzavanja);
            trka.setDuzinaTrke(brKr*staza.getDuzinaKruga());
            trka.setStaza(staza);
            trka.setSezona(yyyy);
            trka.setNajbrziKrug(staza.getNajbrziKrug()+1);
            
            Zahtev z = new Zahtev(Operacija.SACUVAJ_TRKU, trka);
            
            Komunikacija.getInstance().posaljiZahtev(z);
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "Unesite validan broj krugova");
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

